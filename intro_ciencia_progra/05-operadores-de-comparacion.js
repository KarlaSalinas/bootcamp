/*
    Operadores relacionales o de comparación

    >   Mayor que: a > b             a es mayor que b
    <   Menor que: a < b             a es menor que b
    >=  Mayor o igual que: a >= b    a es mayor o igual que b
    <=  Menor o igual que: a <= b    a es menor o igual que b
    ==  Igual
    !=  Distinto 
    === Operador estricto
    !== Operador diferente estricto

*/

let num1 = 22;
let num2 = 15;

console.log('Número 1: ' + num1);
console.log('Número 2: ' + num2);
console.log('Mayor que: ',  num1 > num2);
console.log('Menor que: ', num1 < num2);
console.log('Mayor o igual que: ', num1 >= num2);
console.log('Menor o igual que: ', num1 <= num2);
console.log('Igual: ', num1 == num2);
console.log('Diferente: '+ (num1 != num2));